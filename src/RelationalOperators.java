
public class RelationalOperators {
	public static void main(String[] args) {
		int a =10;
		int b=11;
		System.out.println(a==b);
		System.out.println(a<b);
		boolean lessThan =a<b;
		System.out.println(lessThan);
		System.out.println(a>b);
		
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
		
		
	}

}
